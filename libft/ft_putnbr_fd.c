#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c_nb;
	long	nb;

	nb = n;
	if (nb < 0)
	{
		nb *= -1;
		write(fd, "-", 1);
	}
	if (nb >= 10)
		ft_putnbr_fd(nb / 10, fd);
	c_nb = '0' + (nb % 10);
	write(fd, &c_nb, 1);
}
