#include "libft.h"

int	ft_strcmp_char(char *str1, char *str2, char c)
{
	while (*str1 && *str2 && *str1 == *str2 && *str1 != c && *str2 != c)
	{
		str1++;
		str2++;
	}
	return (*str1 - *str2);
}
